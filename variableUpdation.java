package A1_Basics;

public class variableUpdation {

    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x=7;
        System.out.println(x);
        x=x+6;//13
        System.out.println(x);
        x= x - 20;//-7
        System.out.println(x);
    }
}
